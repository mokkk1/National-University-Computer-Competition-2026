def _load_static_industry():
    """加载静态行业映射（内嵌字典 + 可选CSV文件）"""
    industry_map = dict(_STATIC_INDUSTRY)

    # 如果有CSV文件，追加加载
    if os.path.exists(STATIC_INDUSTRY_FILE):
        try:
            csv_df = pd.read_csv(STATIC_INDUSTRY_FILE)
            if '股票代码' in csv_df.columns and '行业' in csv_df.columns:
                for _, row in csv_df.iterrows():
                    code = str(row['股票代码']).zfill(6)
                    industry = str(row['行业']).strip()
                    if industry and industry in INDUSTRY_MAP:
                        industry_map[code] = industry
                print(f"  从CSV加载行业映射: {len(csv_df)} 条")
        except Exception:
            pass

    return industry_map


def build_industry_map(stock_df):
    """
    构建股票代码 → 行业名称的映射（修复版）。

    策略（多级回退）：
      1. 静态映射表（最可靠，覆盖主要HS300成分股）
      2. AKShare API（过滤指数板块，只查询真正的行业板块）
      3. 股票代码前缀推断（最后手段）
    """
    global _industry_cache
    if _industry_cache is not None:
        return _industry_cache

    code_to_industry = {}

    # ─── Level 1: 静态映射 ─────────────────────────
    static_map = _load_static_industry()
    code_to_industry.update(static_map)
    print(f"  静态行业映射: {len(code_to_industry)} 只股票")

    # ─── Level 2: AKShare API 补充 ─────────────────
    try:
        import akshare as ak

        # Step A: 获取行业板块列表
        board_df = ak.stock_board_industry_name_em()
        if board_df is not None and len(board_df) > 0:
            board_names = board_df['板块名称'].tolist()

            # 过滤掉指数板块、概念板块等
            industry_boards = [
                name for name in board_names
                if name not in _INDEX_BOARDS
                and not any(kw in name for kw in ['指数', '成份', '概念', '风格', '昨日', '近期'])
            ]

            print(f"  发现 {len(industry_boards)} 个行业板块（过滤后）")

            # Step B: 逐个查询行业板块成员
            new_count = 0
            for board_name in industry_boards[:100]:  # 限制查询数量，避免太慢
                if len(code_to_industry) >= 300:  # 已经覆盖了大部分
                    break
                try:
                    members = ak.stock_board_industry_cons_em(symbol=board_name)
                    if members is not None and len(members) > 0:
                        for _, m in members.iterrows():
                            code = str(m.get('代码', '')).zfill(6)
                            if code not in code_to_industry:
                                # 映射到申万行业分类
                                mapped = _map_to_shenwan(board_name)
                                code_to_industry[code] = mapped
                                new_count += 1
                except Exception:
                    pass

            if new_count > 0:
                print(f"  从 AKShare 补充: {new_count} 只股票")
    except Exception as e:
        print(f"  AKShare API 不可用: {e}")

    # ─── Level 3: 代码前缀推断 ─────────────────────
    # 对仍未匹配的股票，用代码段做简单推断
    # 600/601/603开头为主板，000/002开头为深市
    # 无法精确判断行业，标记为"综合"
    if len(code_to_industry) < 100:
        all_codes = stock_df['股票代码'].unique()
        for code in all_codes:
            code_str = str(int(code)) if isinstance(code, float) else str(code)
            code_str = code_str.zfill(6)
            if code_str not in code_to_industry:
                code_to_industry[code_str] = '综合'

    _industry_cache = code_to_industry
    return code_to_industry


