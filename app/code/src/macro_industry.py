def load_macro_features():
    """加载宏观特征 DataFrame（日期索引）"""
    if not os.path.exists(MACRO_FILE):
        print(f"⚠️ 宏观数据文件不存在: {MACRO_FILE}")
        print("   请先运行: python fetch_macro.py --force")
        return None

    df = pd.read_csv(MACRO_FILE, parse_dates=['日期'])
    df['日期'] = pd.to_datetime(df['日期'])
    print(f"✅ 加载宏观特征: {len(df.columns)-1} 维, {len(df)} 行")
    return df


def merge_macro_to_stock(stock_df, macro_df):
    """
    将宏观特征拼接到股票 DataFrame。

    Args:
        stock_df: 股票行情数据，需包含 '日期' 列
        macro_df: 宏观数据，需包含 '日期' 列

    Returns:
        拼接后的 DataFrame，宏观特征按日期对齐
    """
    if macro_df is None:
        return stock_df

    stock_df = stock_df.copy()
    stock_df['日期'] = pd.to_datetime(stock_df['日期'])

    macro_cols = [c for c in macro_df.columns if c != '日期']

    merged = pd.merge(stock_df, macro_df, on='日期', how='left')

    # 前向填充（处理节假日无宏观数据的情况）
    for col in macro_cols:
        merged[col] = merged[col].ffill().fillna(0)

    print(f"  拼接宏观特征: {len(macro_cols)} 维 → 总列数 {len(merged.columns)}")
    return merged


def add_industry_features(stock_df):
    """
    为股票 DataFrame 添加行业编码。

    Args:
        stock_df: 需包含 '股票代码' 列

    Returns:
        (stock_df_with_industry, industry_map)
        - industry 列: 0 ~ NUM_INDUSTRIES-1 的整数编码
        - 未匹配的标记为 0（银行）
    """
    stock_df = stock_df.copy()
    code_to_industry = build_industry_map(stock_df)

    # 初始化 industry 列
    stock_df['industry'] = 0  # 默认银行

    # 从股票代码映射行业
    for code in stock_df['股票代码'].unique():
        code_str = str(int(code)) if isinstance(code, float) else str(code)
        code_str = code_str.zfill(6)
        industry_name = code_to_industry.get(code_str, '综合')
        industry_idx = INDUSTRY_MAP.get(industry_name, 0)
        stock_df.loc[stock_df['股票代码'] == code, 'industry'] = industry_idx

    n_industries = stock_df['industry'].nunique()
    n_mapped = len(code_to_industry)
    print(f"  行业分类: {n_industries} 个不同行业, {n_mapped} 只股票已映射, {len(stock_df)} 行数据")

    return stock_df, INDUSTRY_MAP


class IndustryEmbedding(nn.Module):
    """
    行业 Embedding 层。

    将整数行业编码映射为可学习的稠密向量，
    在 Cross-Stock Attention 之前与股票特征拼接。
    """
    def __init__(self, num_industries=NUM_INDUSTRIES, emb_dim=16):
        super().__init__()
        self.embedding = nn.Embedding(num_industries, emb_dim)
        self.num_industries = num_industries
        self.emb_dim = emb_dim

    def forward(self, industry_ids):
        """
        Args:
            industry_ids: [B, N] 整数张量，每只股票的行业编码

        Returns:
            [B, N, emb_dim] 行业嵌入向量
        """
        return self.embedding(industry_ids)


